letters = ['a', 'b', 'c', 'd', 'e', 'f', 'g']
print("letters[1:3] value is: {0}".format(letters[1:3]))
print("letters[1:-4] value is: {0}".format(letters[1:-4]))
print("letters[:4] value is: {0}".format(letters[:4]))
print("letters[4:] value is: {0}".format(letters[4:]))
copy = letters[:]
print("copy from letters, the new letters: {0}".format(copy))
